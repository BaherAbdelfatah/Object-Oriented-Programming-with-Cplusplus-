#include "Calculator.h"


Calculator::Calculator(void)
{
}


Calculator::~Calculator(void)
{
}

int Calculator::add(int num1, int num2)
{
	return num1 + num2;
}

int Calculator::multiply(int num1, int num2)
{
	return num1 * num2;
}

int Calculator::subtract(int num1, int num2)
{
	return num1 - num2;
}